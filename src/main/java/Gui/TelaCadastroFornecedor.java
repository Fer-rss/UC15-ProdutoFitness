            private void configurarTabela() {
                modeloTabela = (DefaultTableModel) tblFornecedor.getModel();
                modeloTabela.setRowCount(0);
            }

            private void atualizarTabela() {
                modeloTabela.setRowCount(0);

                for (Fornecedor f : fornecedorService.listar()) {
                    modeloTabela.addRow(new Object[]{
                        f, // OBJETO
                        f.getCnpj(),
                        f.getPrazoEntrega()
                    });
                }
            }

            private void limparCampos() {
                txtNome.setText("");
                txtCnpj.setText("");
                txtPrazo.setText("");
            }
            
            private Fornecedor lerFornecedorDaTela() throws Exception {
                String nome = txtNome.getText().trim();
                String cnpj = txtCnpj.getText().trim();
                String prazo = txtPrazo.getText().trim();

                if (nome.isEmpty() || cnpj.isEmpty()) {
                    throw new Exception("Preencha Nome e CNPJ!");
                }

                Fornecedor fornecedor = new Fornecedor();
                fornecedor.setNomeFornecedor(nome);
                fornecedor.setCnpj(cnpj);
                fornecedor.setPrazoEntrega(prazo);

                return fornecedor;
            }

