    private void btAdicionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btAdicionarActionPerformed
        try {
        Fornecedor fornecedor = lerFornecedorDaTela();
        
        // Adicionar à lista     
        fornecedorService.salvar(fornecedor);
        JOptionPane.showMessageDialog(this, "Fornecedor cadastrado com sucesso!");
               
                // Atualizar a tabela
                atualizarTabela();

                // Limpar os campos
                limparCampos(); 
                
        } catch (Exception e) {
                JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }//GEN-LAST:event_btAdicionarActionPerformed

    private void btAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btAlterarActionPerformed
        int linha = tblFornecedor.getSelectedRow();

            if (linha < 0) {
                JOptionPane.showMessageDialog(this, "Selecione um fornecedor para alterar!");
                return;
            }

            try {
                Fornecedor fornecedorSelecionado =
                    (Fornecedor) modeloTabela.getValueAt(linha, 0);

                Fornecedor atualizado = lerFornecedorDaTela();
                atualizado.setId(fornecedorSelecionado.getId());

                fornecedorService.atualizar(atualizado);

                JOptionPane.showMessageDialog(this, "Fornecedor alterado com sucesso!");

                atualizarTabela();
                limparCampos();

            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, e.getMessage());
            }
    }//GEN-LAST:event_btAlterarActionPerformed

    private void btExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btExcluirActionPerformed
        int linha = tblFornecedor.getSelectedRow();

        if (linha < 0) {
            JOptionPane.showMessageDialog(this, "Selecione um fornecedor para excluir.");
            return;
        }

        Fornecedor fornecedor = (Fornecedor) tblFornecedor.getValueAt(linha, 0);

        int resposta = JOptionPane.showConfirmDialog(
            this,
            "Tem certeza que deseja remover o fornecedor \"" + fornecedor.getNomeFornecedor() + "\"?",
            "Confirmação",
            JOptionPane.YES_NO_OPTION
        );

        if (resposta == JOptionPane.YES_OPTION) {
            try {
                fornecedorService.excluir(fornecedor);
                JOptionPane.showMessageDialog(this, "Fornecedor excluído com sucesso!");

            // Atualizar a tabela
            atualizarTabela();

            // Limpar os campos
            limparCampos(); 

        } catch (Exception e) {
            JOptionPane.showMessageDialog(
                this,
                "Erro ao excluir fornecedor:\n" + e.getMessage(),
                "Erro",
                JOptionPane.ERROR_MESSAGE
            );
        }
    }
    }//GEN-LAST:event_btExcluirActionPerformed

