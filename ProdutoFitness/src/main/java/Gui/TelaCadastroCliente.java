            private void configurarTabela() {
                modeloTabela = (DefaultTableModel) tblClientes.getModel();
                modeloTabela.setRowCount(0);
            }

            private void atualizarTabela() {
                modeloTabela.setRowCount(0);
                listaClientes = clienteService.listar();

                for (Cliente c : listaClientes) {
                    modeloTabela.addRow(new Object[]{
                        c, // OBJETO Cliente (OBRIGATÓRIO)
                        c.getCpf(),
                        c.getEndereco(),
                        c.getEmail(),
                        c.getTelefone()
                    });
                }
            }

            private void limparCampos() {
                txtNome.setText("");
                txtCpf.setText("");
                txtEndereco.setText("");
                txtEmail.setText("");
                txtTelefone.setText("");
            }
            
            private Cliente lerClienteDaTela() {
                Cliente c = new Cliente();
                c.setNomeCliente(txtNome.getText().trim());
                c.setCpf(txtCpf.getText().trim());
                c.setEndereco(txtEndereco.getText().trim());
                c.setEmail(txtEmail.getText().trim());
                c.setTelefone(txtTelefone.getText().trim());
                return c;
            }
            
            private boolean validarCamposObrigatorios() {
                if (txtNome.getText().trim().isEmpty()) {
                    JOptionPane.showMessageDialog(this, "Informe o nome do cliente.");
                    txtNome.requestFocus();
                    return false;
                }

                if (txtCpf.getText().contains("_")) {
                    JOptionPane.showMessageDialog(this, "Informe um CPF válido.");
                    txtCpf.requestFocus();
                    return false;
                }

                return true;
            }

