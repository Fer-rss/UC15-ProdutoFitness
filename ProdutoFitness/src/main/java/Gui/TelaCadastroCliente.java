    private void btExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btExcluirActionPerformed
        if (clienteSelecionado == null) {
            JOptionPane.showMessageDialog(this, "Selecione um cliente para excluir.");
            return;
        }

        int resposta = JOptionPane.showConfirmDialog(
            this,
            "Deseja realmente excluir o cliente \"" + clienteSelecionado.getNomeCliente() + "\"?",
            "Confirmação",
            JOptionPane.YES_NO_OPTION
        );

        if (resposta == JOptionPane.YES_OPTION) {
            clienteService.excluir(clienteSelecionado.getId());
            JOptionPane.showMessageDialog(this, "Cliente excluído com sucesso.");

            // Atualizar a tabela
            atualizarTabela();

            // Limpar os campos
            limparCampos(); 
            clienteSelecionado = null;
        }
    }//GEN-LAST:event_btExcluirActionPerformed

    private void btAdicionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btAdicionarActionPerformed
        if (!validarCamposObrigatorios()) return;

            Cliente cliente = lerClienteDaTela();
            
            // Adicionar à lista 
            clienteService.salvar(cliente);

            JOptionPane.showMessageDialog(this, "Cliente cadastrado com sucesso.");

            // Atualizar a tabela
            atualizarTabela();

            // Limpar os campos
            limparCampos(); 
    }//GEN-LAST:event_btAdicionarActionPerformed

    private void btAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btAlterarActionPerformed
        if (clienteSelecionado == null) {
            JOptionPane.showMessageDialog(this, "Selecione um cliente na tabela.");
            return;
        }
        if (!validarCamposObrigatorios()) return;

        try {
            Cliente cliente = lerClienteDaTela();
            cliente.setId(clienteSelecionado.getId()); 
            clienteService.atualizar(cliente);

            JOptionPane.showMessageDialog(this, "Cliente alterado com sucesso.");
            // Atualizar a tabela
            atualizarTabela();

            // Limpar os campos
            limparCampos(); 
            clienteSelecionado = null;
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }//GEN-LAST:event_btAlterarActionPerformed

